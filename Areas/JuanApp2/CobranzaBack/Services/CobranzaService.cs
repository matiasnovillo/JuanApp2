        #region Importations
        public List<Cobranza> ImportExcel(string path, int userId)
        {
            List<Cobranza> lstCobranza = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    int CobradorId = Convert.ToInt32(row.Cell(7).GetString());
                    decimal DineroBanco = Convert.ToDecimal(row.Cell(8).GetString());
                    decimal DineroCheque = Convert.ToDecimal(row.Cell(9).GetString());
                    decimal DineroEfectivo = Convert.ToDecimal(row.Cell(10).GetString());
                    decimal DineroTotal = Convert.ToDecimal(row.Cell(11).GetString());
                    

                    Cobranza Cobranza = new()
                    {
                        CobranzaId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        CobradorId = CobradorId,
                        DineroBanco = DineroBanco,
                        DineroCheque = DineroCheque,
                        DineroEfectivo = DineroEfectivo,
                        DineroTotal = DineroTotal,
                        
                    };

                    lstCobranza.Add(Cobranza);
                }
            }

            return lstCobranza;
        }
