        #region Importations
        public List<Proveedor> ImportExcel(string path, int userId)
        {
            List<Proveedor> lstProveedor = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    string NombreCompleto = row.Cell(7).GetString();
                    string Celular = row.Cell(8).GetString();
                    string Direccion = row.Cell(9).GetString();
                    string PaginaWeb = row.Cell(10).GetString();
                    

                    Proveedor Proveedor = new()
                    {
                        ProveedorId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        NombreCompleto = NombreCompleto,
                        Celular = Celular,
                        Direccion = Direccion,
                        PaginaWeb = PaginaWeb,
                        
                    };

                    lstProveedor.Add(Proveedor);
                }
            }

            return lstProveedor;
        }
