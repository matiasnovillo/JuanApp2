        #region Importations
        public List<ModuloGasto> ImportExcel(string path, int userId)
        {
            List<ModuloGasto> lstModuloGasto = [];

            var WorkBook = new XLWorkbook(path);
            var Rows = WorkBook.Worksheet(1).RangeUsed().RowsUsed();

            foreach (var row in Rows)
            {
                var rowNumber = row.RowNumber();

                if (rowNumber > 1)
                {
                    DateTime Fecha = Convert.ToDateTime(row.Cell(7).GetString());
                    string Descripcion = row.Cell(8).GetString();
                    decimal DineroEfectivo = Convert.ToDecimal(row.Cell(9).GetString());
                    decimal DineroCheque = Convert.ToDecimal(row.Cell(10).GetString());
                    decimal DineroBanco = Convert.ToDecimal(row.Cell(11).GetString());
                    decimal DineroTotal = Convert.ToDecimal(row.Cell(12).GetString());
                    

                    ModuloGasto ModuloGasto = new()
                    {
                        ModuloGastoId = 0,
                        Active = true,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        UserCreationId = userId,
                        UserLastModificationId = userId,
                        Fecha = Fecha,
                        Descripcion = Descripcion,
                        DineroEfectivo = DineroEfectivo,
                        DineroCheque = DineroCheque,
                        DineroBanco = DineroBanco,
                        DineroTotal = DineroTotal,
                        
                    };

                    lstModuloGasto.Add(ModuloGasto);
                }
            }

            return lstModuloGasto;
        }
